 public class Borsellino {

    int valore ;

    String titolare;

    public Borsellino(){
        this.valore = 0;
        this.titolare = "";
    }

    public Borsellino(int v, String s) {
        valore = v;
        titolare = s;
    }

     public int getValore() {
         return valore;
     }

 }


